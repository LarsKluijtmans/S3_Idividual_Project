CREATE TABLE product(
    id   int     NOT NULL AUTO_INCREMENT,
    title char(50) NOT NULL,
    sub_title varchar(50),
    series varchar(30),
    year int,
    price double,
    condition_ enum('TRASH','VERY_BAD','BAD','NOT_BAD','OK','GOOD','GREAT','EXCELLENT'),
    description varchar(500),
    genre enum('BOARD_GAME','CARD_GAME','CASINO_GAME','DIGITAL_COLLECTIBLE_CARD_GAME','GACHA_GAME','HORROR_GAME','IDLE_GAME','LOGIC_GAME','PARTY_GAME','PHOTOGRAPHY_GAME',
        'PROGRAMMING_GAME','SOCIAL_DEDUCTION_GAME','TRIVIA_GAME','TYPING_GAME','ADVERGAME','ART_GAME','CASUAL_GAME','CHRISTIAN_GAME','EDUCATIONAL_GAME','ESPORTS','EXERGAME',
        'PERSONALIZED_GAME', 'SERIOUS_GAME', 'ACTION','PLATFORM_GAMES','SHOOTER_GAMES','FIGHTING_GAMES','BEAT_EM_UP_GAME','STEALTH_GAME','SURVIVAL_GAMES','RHYTHM_GAMES','BATTLE_ROYAL_GAMES',
        'ACTION_ADVENTURE','SURVIVAL_HORROR','METROPOLITAN','ADVENTURE','TEXT_ADVENTURE','GRAPHIC_ADVENTURES','VISUAL_NOVELS','INTERACTIVE_MOVIE','REAL_TIME_3D_ADVENTURE','PUZZLE',
        'BREAKOUT_CLONE_GAME','LOGICAL_GAME','PHYSICS_GAME','CODING_GAME','TRIAL_AND_ERROR_EXPLORATION','HIDDEN_OBJECT_GAME','REVEAL_THE_PICTURE_GAME','TITLE_MATCHING_GAME',
        'TRADITIONAL_PUZZLE_GAME','ROLE_PLAYING','ACTION_RPG','MMORPG','ROGUELIKE','TACTICAL_RPG','Sandbox_RPG','FIRST_PERSON_PARTY_BASED_RPG','JRPG','MONSTER_TAMER','SIMULATION',
        'CONSTRUCTION_AND_MANAGEMENT_SIMULATION','Life _SIMULATION','VEHICLE_SIMULATION','4X_GAME','ARTILLERY_GAME','AUTO_BATTLER_(AUTO_CHESS)','MULTIPLAYER_ONLINE_BATTLE_ARENA_(MOBA)',
        'REAL_TIME_STRATEGY_(RTS)','REAL_TIME_TACTICS_(RTT)','TOWER_ DEFENSE','TURN_BASED_STRATEGY_(TBS)','TURN_BASED_TACTICS_(TBT)','WARGAME','GRAND_STRATEGY_WAR_GAME','RACING','SPORTS_GAME',
        'COMPETITIVE','SPORT_BASED_FIGHTING','MMO'),
    sold boolean,
    product_type enum('GAME', 'GAMES'),
    PRIMARY KEY (id)
);

CREATE TABLE images(
    id   int     NOT NULL AUTO_INCREMENT,
    image_url char(200) NOT NULL,
    product_Id int NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (product_Id) references product(id)
);